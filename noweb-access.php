С ЭТОГО КОМПЬЮТЕРА (ЛОКАЛЬНО), виртуальная консоль Alt-F1:
-----------------------------------------------------------------------------
  - ЛОКАЛЬНЫЙ ПРОСМОТР ВИДЕОКАМЕР - программа МОНИТОР
      Alt-F1 - переключиться в консоль оператора и выполнить команду 'mon'

  - НАСТРОЙКА/(ПЕРЕ)ЗАПУСК videoserv
      запустите тектовый браузер 'www' в консоли оператора
