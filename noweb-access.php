      Alt-F2, затем стрелками вверх/вниз, клавиша M - Mute/UnMute

  - ВХОД ДЛЯ СПЕЦ. ОБСЛУЖИВАНИЯ
      Alt-F3 - вход в систему как администратор root для спец. обслуживания

  - ПРОСМОТР СИСТЕМНОГО ЖУРНАЛА
      Alt-F4 - просмотр журнала операций

  - ПЕРЕЗАПУСК или ВЫКЛЮЧЕНИЕ КОМПЬЮТЕРА выполните команды
