<h2>Справка для администратора - суперпользователя <font color="darkRed">root</font>.</h2>
<pre>
виртуальная консоль Alt-F1
или через telnet
-----------------------------------------------------------------------------
Как суперпользователь root Вы можете:
-----------------------------------------------------------------------------
  - сменить пароли пользователей root(свой) и пользователя ftptelnet
      для этого нужно выполнить команду 'luser-cfg'

  - настроить драйвер плат видеозахвата
      выполните команду 'tuner-cfg.sh'

  - изменить сетевые настройки
      выполните команду 'chip.sh'

  - настроить системные часы и синхронизацию времени
      выполните команду 'setup-clock.sh'

  - ПЕРЕЗАПУСК или ВЫКЛЮЧЕНИЕ КОМПЬЮТЕРА выполните команды
      выполните команды 'reboot' или 'poweroff'

  - удаленный вход как root
      по сети подключитесь через telnet
      и войдите под пользователем ftptelnet
      наберите команду 'su -' и введите пароль суперпользователя root

  - установка загрузчика LinuxDVR на HDD или USB-флэш диски
     выполните команду 'install-boot'

  - выход из сеанса root, то же и для др. пользователей вкл. ftptelnet
     выполните команды 'exit' или нажмите CTRL-D

    дополнительно доступно много штатных утилит Linux,
    таких как 'date', 'mc', 'cat', 'ls'
</pre>

